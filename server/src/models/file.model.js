import mongoose from "mongoose";

const fileSchema = new mongoose.Schema(
    {
        _id: String,
        title: {
            type: String,
            required: true,
            trim: true,
        },
        description: {
            type: String,
            required: true,
            trim: true,
        },
        file_path: {
            type: String,
            required: true,
        },
        file_mimetype: {
            type: String,
            required: true,
        },
    },
    {
        timestamps: true,
    }
);

const File = mongoose.model("File", fileSchema);

export default File;
